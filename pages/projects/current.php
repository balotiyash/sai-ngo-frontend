            <p><b>SAI-VIHAAN  PROJECT-</b></p>
            <p>The specific objectives of Vihaan include:</p>
            <ol>
                <li>Early linkages of PLHIV to Care ,Support and treatment services</li>
                <li>Improved treatment adherence and education for PLHIV</li>
                <li>Expanded positive prevention activities</li>
                <li>Improved social protection and wellbeing of PLHIV</li>
                <li>Strengthened community systems and reduced stigma and discrimination</li>
            </ol>

            <br>

            <p>Under this project we have providing Care & Support to 15000 PLHIV registered in main 3 Public ART Centres at Viz. J.J. , Nair & KEM Hospitals. In  this project  the  said  people are Personally   contacted  by our  Outreach  workers,  counselor  and  doing  the awareness  task  and motivating  them  for  ICTC  Tests.  ART, periodical check-up in  the Public  Hospitals  also bringing  back those clients  who have  discontinued  the  ART from  the Public  Hospitals. In addition to this the  project  is  also  providing  the  assistance  to  these  people  in  getting  the benefits of  the Govt .Schemes  for them. <b>The project is funded by  NMP+Pune since 2015.</b></p>

            <br>

            <p><b>CANCER AID CELL-</b> From  2013  we have  started  providing  the  financial  help  to  the poor  cancer patients. The said  help is  given to  the poor  and  needy  patients  by  verifying  the  records  such  as the Cost certificate, medical reports  of  the  Hospital, Income proof, KYC norms  documents  i.e. Aadhar, pancard etc. And  after completing  our usual  formalities  we  are extending the  help  to  them. During      the      period   from 2013 to till date we have extended   the financial  help   to  150 Cancer patients. <b>We are very thankful to generous individual donors & our fund raiser activists for this cause.</b></p>

            <br>

            <p><b>HEPATITS  B   PROJECT  ( PAHEL  PROJECT) –</b>   In the project we  are  providing  the free health care services to the poor  female sex workers,  pregnant  women  ,  women  in  child bearing age, their family  members  and    residing  in  A,B,C,D & E wards of  Mumbai for  the prevention and  treatment of Hepatitis  B. Our  Out  Reach  Workers   are personally contacting  them and  educating  them about  the awareness of  the disease  and referring them for the necessary  test  in  the Public Hospitals. After the  testing  those women  who  are found  as Negative  are  vaccinated  and  Positive women are given  the required  treatment in the Public Hospitals. As  of  now  569 women  are  registered with  us  and  they are under our active follow-up.</p>

            <p><b>We  have  started  this  project  from  01–03-2017 with funding  support  of  UNITED WAY  MUMBAI  a renowned  NGO having global network.</b></p>

            <br>

            <p><b>PROJECT  OF MANUFACTURING  OF  ECO-FREINDLY  WOODEN  STOVE–</b></p>
            <p>We are  manufacturing  of   wood fuel  stoves. We have designed  an  ecofriendly  Wood  fuel  stove  which  is  generating  bare  minimum  smoke  and  emission.  By  using  this  stove  we  can protect  the  actual  users  mainly  women in  India  from  the  diseases  like Cough, TB, Eye –disorder,  throat  infections  etc.  we  have proposed  to  market  these  stoves  in the Rural  sector   and  Tribal  areas  at  the  moderate  and  affordable  rates  to  this  poor   community and  protect  their  health  and  environment  as  well.  Our  generous  funding  partner  viz. C E D-Finland  have  provided  us  PEMS  Machinery  ( U S A Made) free  of  cost  for  the  Emission  testing  of these  stoves.  We  have  conducted  4  market  survey  for  these  stoves  in  the  Rural  and  Tribal  areas  of  Thane  and  Palghar  Districts  nearby  to  Mumbai  and  found  that  the  said  stove  is  very much  appreciated  by  the  Rural  and  Tribal  women  and  inclined  to  use  the  same  for  their daily cooking  purpose.  We  have  aimed  to sell  these  stoves  to  the  said  women Group  at affordable cost  and the  income  so  generated  from  it  shall  be  used for  the  medical , nutritional  support of  the  Key  Population  of  our  abovesaid   Target  population/community.</p>

