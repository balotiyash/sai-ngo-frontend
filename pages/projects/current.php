            <p><b>REAR Project- ( Educational support for Children of Sex Workers)-</b></p>
            <p>This project aims to break the intergenerational cycle of prostitution and ensures that discriminated and marginalised children of commercial sex workers (CSWs) have the opportunities towards a healthy and safe future.</p>
            <p>Under this project we are doing following activities:</p>

            <ul>
                <li>Run the after-school education club.</li>
                <li>Conduct regular tests to assess children’s progress.</li>
                <li>Provide education (material) support to identified children.</li>
                <li>Provide after-school meals to ensure children obtain adequate nutrition.</li>
                <li>Assist children with school curricula.</li>
                <li>Encourage children to pursue hobbies.</li>
            </ul>

            <p>Due to these activities we have brought more than 500 children of sex workers in education stream & stopped child prostitution.</p>

