            <p><b>Awareness project-</b> Street plays, slide shows, Magic shows, pamphlet distribution, poster exhibitions being conducted all over the country. More than 5000 shows already conducted for the prevention of HIV/AIDS, TB, Hepatitis and to diminishing stigma and discrimination related to these diseases.</p>

