            <h1>Past Projects</h1>

            <p><b>Suraksha-</b> Implemented HIV prevention and treatment of sexually transmitted diseases for 2200 floating/street based sex workers since 2004 to 2016. Recruited staff and peer staff from the community and trained to tackle the HIV prevention & reduce the HIV prevalence. <b>The project was funded by Family Health International through Bill & Melinda Gates Foundation since 2004 to 2012 under Aastha Ptoject & thereafter funded by Mumbai District AIDS Control Society (Mumbai) viz. Suraksha.</b></p>

            <br>

            <p><b>Prevention of Parents to Child Transmission (PPTCT)-</b> Prevention of HIV in newborn of HIV infected mothers by strategic intervention of drugs and support programme. 10 widows living with HIV/AIDS and other PLHAs were part of this project.These peers go door to door and create awareness about HIV/AIDS and prevention of parent to child transmission in in A,B, C, F-South & G- South wards of Municipal Corporation of Greater Mumbai (MCGM). They rapidly surveyed the areas and found pregnant women and motivated them to get themselves registered in government hospitals where they were counseled for ICTC and if found positive the spouse was also motivated to undergo the ICTC. If the mother was found to be positive the Out Reach Worker took full responsibility and created awareness for the need of ART, which was given to them and the child after the delivery. This had helped in rapidly preventing the spread of disease from mother to child. SAI has prevented 310 children from getting HIV infection from their HIV infected pregnant mothers since 2006 to 2015. <b>It was funded by Mumbai District AIDS Control Society & National AIDS Control Organisation (NACO)</b></p>

            <br>

            <p><b>Community Care Centre (CCC)-</b> Played a critical role in providing treatment, care and support to people living with HIV/AIDS (PLHA) in Thane district Under NACP-III this centre was attached to 4 ART centres and ensured that 5000 PLHA were provided:</p>

            <ul>
                <li>counselling for ART drug adherence, nutrition and prevention</li>
                <li>treatment of opportunistic infection- in patients care/ OPD</li>
                <li>referral and outreach services for follow up</li>
                <li>social support services</li>
            </ul>

            <p>The centre was mandated to seek better community and family response towards PLHA through family counselling for better treatment outcome, the centre provided families of PLHA counselling on the patients nutritional needs, treatment adherence and psychological support.</p>

            <p>CCC had better linkages and referrals to ICTC, DOTS, PPTCT, LAC, other treatment services and interventions. CCC served as a vital link in providing holistic support to PLHA with district hospitals and provided referral service to PLHA when needed. <b>This project was funded by NACO & Karnataka Health Promotion Trust implemented since 2010 to 2015.</b></p>

            <br>

            <p><b>Mobile ICTC in Thane district–</b> This mobile ICTC/STI clinic was providing ‘services-on-wheels’ to the High risk group which included, Female sex workers, migrants, MSM,IDU in collaboration with other 15 NGOs working with 22500 High risk population in Thane District., who were not particularly inclined to visit the static ICTCs. The advantage of running a Mobile ICTC/STI clinic was that it enabled to reach out to hard-to-reach spots and at the same covering very wide areas. Mobile ICTC also helped these High risk group to overcome the cost and inconvenience of having to travel to centers for HIV counseling and testing and spending a lot of time. The mobile clinic was providing following services:</p>

            <p>A Mobile ICTC Clinic had solved many of the above difficulties faced by High risk group through following ways</p>

            <ul>
                <li>Availability of ICTC services within the easy accessibility of High risk group.</li>
                <li>Coverage in collaboration with other NGOs of vast geographical areas in Thane district, where large number of High risk group was able to reach without loss of their pay and their convenience.</li>
                <li>Carrying out preplanned Mobile ICTC clinic activities at most convenient place and most suitable time for maximum number of High risk group</li>
                <li>Availability of STI and HIV testing services without unnecessary waiting.</li>
                <li>Availability of rapid HIV test results immediately, avoiding second visit.</li>
                <li>Availability of medical support from the doctor for general health issues.</li>
                <li>Support from the collaborating NGO, wherever required, in getting Care and Support Service and for ART registration at nearby ART Centre.</li>
                <li>Opportunity for High risk group and their clients to avail ICTC services at a given place at the appointed hour.</li>
            </ul>

            <p><b>This project was funded by Avert/ Public Health Foundation of India (PHFI) & implemented since 2012 to 2014</b></p>

            <br>

            <p><b>Pediatric AIDS Initiative (PAI)-</b> PAI project was giving support and care to 50 Children Living With HIV/AIDS (CLHA). The treatment for CLHAs for opportunistic infections and ART was given by NACO (National AIDS control organization) – <b>This project was funded by Sevadham Trust & Clinton Foundation & was implemented during the period from 2007 to 2011</b></p>

            <p>SAI’s role in this project was:</p>

            <ul>
                <li>Diagnosis of HIV infection in infants and children.</li>
                <li>Enumeration of CD4 count in children.</li>
                <li>Pre – ART care, counseling and support for those who don’t qualify for ART.</li>
                <li>Counseling and support for those who require ART.</li>
                <li>Psycho-social support.</li>
                <li>Nutritional support.</li>
                <li>Traveling and logistical help for the children.</li>
            </ul>

            <br>

            <p><b>Certificate Course in Management of HIV/ AIDS</b></p>

            <p>This was devised to make an impact on the HIV AIDS awareness as well as changing the mind-set of the health professional from imminent medical colleges, public hospitals & private sectors were giving health services to PLHAs. It concentrates on giving up-to-date knowledge and motivating the care-givers toward fulfilling their role in the control of HIV scenario & diminishing stigma & discrimination. 15 experts guest faculties from this filed have delivered the presentation with question answers session.</p>

            <p>We have conducted 24 training workshops & 480 participated had attended the same, consisting of 120 each from Medical, Nursing, Para Medical field & Peer Educators, Community Volunteers & stake Holders. <b>This project was funded by Sidaction- France & was implemented in 2008 & 2009.</b></p>

            <br>

            <p><b>Sanman/ Honor – Sanman project was HIV/AIDS preventive program for homosexual and the transgender community funded by Sidaction & Solidarite Sida _France. It was implemented during the period from 2009 to 2016.</b> The sexually minority is largely discriminated and half of this community vulnerable for HIV. This community has formed its own support group and indulges in activities like awareness, safer sex practices, free distribution of condoms- lubrication, ICTC & ART. We had 3000 key population registered under this project. The peers trained in this project were highly motivated for their cause.</p>
